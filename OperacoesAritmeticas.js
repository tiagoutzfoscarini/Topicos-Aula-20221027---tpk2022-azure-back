// Soma
const somar=function(valor1,valor2){
    return valor1+valor2;
}

// Subtração
const subtrair=function(valor1,valor2){
    return valor1-valor2;
}

// Multiplicação
const multiplicar=function(valor1,valor2){
    return valor1*valor2;
}

// Divisão
const dividir=function(valor1,valor2){
    return valor1/valor2;
}

module.exports={
    somar,
    subtrair,
    multiplicar,
    dividir
}